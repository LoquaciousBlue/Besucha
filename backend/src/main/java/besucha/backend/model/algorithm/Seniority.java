/**
 * Seniority enum for student. Consists of Freshman, Sophomore, Junior, and Senior.
 */
public enum Seniority {
	/**
	 * Student may be a freshman.
	 */
	Freshman,

	/**
	 * Student may be a sophomore.
	 */
	Sophomore,

	/**
	 * Student may be a junior.
	 */
	Junior,

	/**
	 * Student may be a senior.
	 */
	Senior
}
